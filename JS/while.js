var NameArr = ["ali", "amir", "mohammad", "hossein"];

var i = 0;

while (NameArr[i]) {
    document.write(NameArr[i] + "<br/>");
    i++;
}

var j = 0;

while (j < NameArr.length) {
    document.write(NameArr[j] + "<br/>");
    j++;
}

var k = 0;

do {
    document.write(NameArr[k] + "<br/>");
    k++;
} while (NameArr[k]);
